// Helper functions
export function getSongsByCategory(category: "Halloween" | "Christmas"): Song[] {
  return songlist.filter((song) => song.category === category);
}

export function getOriginalSongs(): Song[] {
  return songlist.filter((song) => song.isOriginal);
}

export function getVendorSongs(): Song[] {
  return songlist.filter((song) => !song.isOriginal);
}

export function getSongsByYear(year: number): Song[] {
  return songlist.filter((song) => song.yearAdded === year);
}

export function getNewSongs(currentYear: number = 2026): Song[] {
  return songlist.filter((song) => song.yearAdded === currentYear);
}

export function getVendors(): string[] {
  const vendors = new Set(songlist.map((song) => song.vendor));
  return Array.from(vendors).filter((v) => v !== "Lights of Elm Ridge" && v !== "TBD" && v !== "Unknown");
}
